    def get_latest_product_id_by_sku(
        self,
        sku: str,
    ) -> int | None:
        sku = str(
            sku or ""
        ).strip()

        if not sku:
            return None

        sql = """
            SELECT p.id

            FROM product p

            WHERE p.sku = %s

            AND NOT EXISTS (
                SELECT 1
                FROM product hidden_version
                WHERE hidden_version.sku = p.sku
                    AND hidden_version.hidden = true
            )

            ORDER BY p.id DESC
            LIMIT 1
        """

        with get_connection() as conn:
            with conn.cursor() as cursor:
                cursor.execute(
                    sql,
                    (sku,),
                )

                row = cursor.fetchone()

        if row is None:
            return None

        if isinstance(row, dict):
            product_id = row.get("id")
        else:
            product_id = row[0]

        return (
            int(product_id)
            if product_id is not None
            else None
        )

    def count_semantic_vectors(
        self,
    ) -> int:
        sql_table_exists = """
            SELECT EXISTS (
                SELECT 1
                FROM information_schema.tables
                WHERE table_schema = current_schema()
                  AND table_name =
                      'product_semantic_vector'
            ) AS table_exists
        """

        sql_count = """
            SELECT COUNT(*) AS count
            FROM product_semantic_vector
        """

        with get_connection() as conn:
            with conn.cursor() as cursor:
                cursor.execute(
                    sql_table_exists
                )

                row = cursor.fetchone()

                if isinstance(row, dict):
                    table_exists = bool(
                        row.get("table_exists")
                    )
                else:
                    table_exists = bool(
                        row[0]
                    )

                if not table_exists:
                    return 0

                cursor.execute(sql_count)
                row = cursor.fetchone()

        if isinstance(row, dict):
            return int(
                row.get("count", 0)
            )

        return int(row[0])

    def has_product_vector_by_sku(
        self,
        sku: str,
    ) -> bool:
        sku = str(
            sku or ""
        ).strip()

        if not sku:
            return False

        sql = f"""
            SELECT EXISTS (
                SELECT 1

                FROM product_semantic_vector vector_row

                JOIN product product_row
                    ON product_row.id =
                    vector_row.product_id

                INNER JOIN (
                    {self.LATEST_INDEXABLE_PRODUCT_SUBQUERY}
                ) latest
                    ON latest.max_id =
                    product_row.id

                WHERE product_row.sku = %s
            ) AS vector_exists
        """

        try:
            with get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(
                        sql,
                        (sku,),
                    )

                    row = cursor.fetchone()

            if isinstance(row, dict):
                return bool(
                    row.get("vector_exists")
                )

            return bool(row[0])

        except Exception:
            return False
        
    def delete_embeddings_except_product_ids(
        self,
        product_ids: list[int],
    ) -> int:
        normalized_ids = [
            int(product_id)
            for product_id in product_ids
            if product_id is not None
        ]

        if not normalized_ids:
            return self.delete_all_embeddings()

        sql = """
            DELETE FROM product_semantic_vector
            WHERE NOT (
                product_id = ANY(%s)
            )
        """

        with get_connection() as conn:
            with conn.cursor() as cursor:
                cursor.execute(
                    sql,
                    (normalized_ids,),
                )

                deleted = cursor.rowcount

            conn.commit()

        return int(deleted)